// Post-process the swagger-typescript-api output.
//
// The generated low-level client is a raw escape hatch, not part of the
// hand-written SDK's type surface. We prepend `@ts-nocheck` so its generated
// code never blocks the strict typecheck/build of the SDK proper, and add a
// banner noting the file is generated.
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const file = join(here, "..", "src", "generated", "api.ts");

const banner =
  "// @ts-nocheck\n" +
  "/* eslint-disable */\n" +
  "// AUTO-GENERATED by `npm run openapi` (swagger-typescript-api). Do not edit by hand.\n" +
  "// Low-level API client — the ergonomic SDK lives at the package root.\n\n";

const src = readFileSync(file, "utf8");
if (!src.startsWith("// @ts-nocheck")) {
  writeFileSync(file, banner + src);
  console.log("postprocess: prepended @ts-nocheck banner to src/generated/api.ts");
} else {
  console.log("postprocess: banner already present");
}
